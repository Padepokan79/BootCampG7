package example1;

public class Ex3 {
	private String nama;
	private int umur;
	
	public Ex3(String nama, int umur) {
		this.nama = nama;
		this.umur = umur;
	}

	public String getNama() {
		return nama;
	}

	public void setNama(String nama) {
		this.nama = nama;
	}

	public int getUmur() {
		return umur;
	}

	public void setUmur(int umur) {
		this.umur = umur;
	}
}
