package SoalHendraYN;

public class SoalKeempatCreditCard {
	private int iuranTahunan;
	String namaCreditCard;
	
	public SoalKeempatCreditCard() {
		
	}
	
	public SoalKeempatCreditCard(String namaCreditCard, int iuranTahunan) {
		this.namaCreditCard = namaCreditCard;
		this.iuranTahunan = iuranTahunan;
	}

	public int getIuranTahunan() {
		return iuranTahunan;
	}
	
	
}
