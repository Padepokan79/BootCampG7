public class ArrayIntro{
	public static void main(String[] args){
		String[] planets = {"Mercury","Venus","Earth","Mars","Yupiter","Saturn","Uranus","Neptune"};

		for (String p : planets) {
			System.out.println(p + "\t" + p.toUpperCase() );
		}

	}
}